// Use a unique variable name for the select
const bookLangSelect = document.getElementById("langSelect");

function applyBookLanguage(lang) {
  Object.keys(bookTranslations).forEach((id) => {
    const el = document.getElementById(id);
    if (el) {
      if (id === "bookSearch") {
        el.placeholder = bookTranslations[id][lang];
      } else {
        el.textContent = bookTranslations[id][lang];
      }
    }
  });

  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.documentElement.lang = lang;
}

// Handle dropdown change
bookLangSelect.addEventListener("change", () => {
  const selectedLang = bookLangSelect.value;
  localStorage.setItem("lang", selectedLang);
  applyBookLanguage(selectedLang);
});

// On page load
const savedLang = localStorage.getItem("lang") || "en";
bookLangSelect.value = savedLang;
applyBookLanguage(savedLang);
